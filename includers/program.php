                    <h4>Program mentalne aritmetike Suan Pan?</h4>
                    <p>
                        Program <span class="nav-about">SuanPan mentalna aritmetika</span> počinje istovremenom primenom abaka (računaljke) i auditivnog treninga, sledi mentalno usvajanje i postavljanje abakusa u mislima, što dovodi do misaonog izvođenja računskih operacija bez mogućnosti greške i to za vrlo kratko vreme. <br> <br>
    
                        &nbsp &nbsp &nbsp Na kraju svakog završenog nivoa primenjuju se testovi provere pažnje i koncentracije i razvoj svakog učenika se nadgleda. <br> <br>
                    
                        &nbsp &nbsp &nbsp <strong>Program mentalne aritmetike SuanPan</strong> je program mentalnog razvoja dece od 5 do 14 godina. Shodno starosnoj grupi, podeljen je u tri edukativne jedinice: <br>
                        &nbsp &nbsp &nbsp 5 do 7 godina - <strong>KINDER GRUPA</strong> <br>
                        &nbsp &nbsp &nbsp 7 do 10 godina - <strong>PRIMARNA GRUPA</strong> <br>
                        &nbsp &nbsp &nbsp 10 do 14 godina - <strong>SEKUNDARNA GRUPA</strong> <br><br>
                    
                        &nbsp &nbsp &nbsp Obrazovni program se sastoji iz dva dela. Nastavni plan prvog dela obuhvata operacije sabiranja i oduzimanja (osnovni kurs), dok se u drugom delu obrađuju operacije množenja i deljenja (napredni kurs), i sve operacije daju rezultate isključivo uz pomoć misaonog procesa. <br> <br>
                    
                        &nbsp &nbsp &nbsp Trajanje jednog dela (kursa) je osam meseci, odnosno 37 nedelja. Na kraju svakog dela organizovano je polaganje. Deca čiji uspeh nije manji od 70% stiču kvalifikaciju za sledeći nivo. Raspored predavanja obuhvata vezana tri časa, svaki u trajanju od 40 minuta, najmanje jednom nedeljno.
